# Shared setup result type
_SetupResult = Tuple[
    np.ndarray, np.ndarray, np.ndarray,  # integral_sum, integral_sq, integral_count
    int, int,  # min_step, max_step
    Optional[int],  # resolution_hint
    int,  # offset_stride
]


def _setup_reconstruction(
    img: Image.Image,
    width: int,
    height: int,
    config: Config,
) -> Optional[_SetupResult]:
    """Shared setup for reconstruction-based grid detection.

    Converts image to perceptual color space, builds integral images,
    and determines the step size search range.

    Returns None if the image is too small or the search range is empty.
    """
    if width < 4 or height < 4:
        return None

    arr = np.array(img, dtype=np.uint8)
    alpha = arr[:, :, 3] if arr.shape[2] == 4 else np.ones((height, width), dtype=np.uint8) * 255
    mask = alpha > 0

    lab = _rgb_to_approx_lab(arr)
    integral_sum, integral_sq, integral_count = _build_integral_images(lab, mask)

    max_dim = max(width, height)
    min_dim = min(width, height)
    config_min = max(3, config.reconstruction_min_step)
    config_max = config.reconstruction_max_step

    min_step = max(config_min, max_dim // 128)
    max_step = min(min_dim // 2, config_max, max_dim // 4)

    if max_step < min_step:
        return None

    resolution_hint = config.resolution_hint
    offset_stride = 2 if max_step > 32 else 1

    return (
        integral_sum, integral_sq, integral_count,
        min_step, max_step,
        resolution_hint, offset_stride,
    )


def _coarse_search_and_refine(
    integral_sum: np.ndarray,
    integral_sq: np.ndarray,
    integral_count: np.ndarray,
    width: int,
    height: int,
    min_step: int,
    max_step: int,
    offset_stride: int,
    resolution_hint: Optional[int],
    max_candidates: int,
) -> List[Tuple[float, float, float, float]]:
    """Run coarse grid search + Nelder-Mead refinement on top results.

    Returns refined (error, step, ox, oy) tuples sorted by error,
    or empty list if no candidates found.
    """
    logger.debug(
        f"Reconstruction: coarse search step=[{min_step}..{max_step}], "
        f"offset_stride={offset_stride}"
    )
    coarse_results = _coarse_grid_search(
        integral_sum, integral_sq, integral_count,
        width, height, min_step, max_step,
        offset_stride=offset_stride,
        resolution_hint=resolution_hint,
    )

    if not coarse_results:
        return []

    n_refine = min(max(max_candidates, 3), len(coarse_results), 8)
    top_coarse = coarse_results[:n_refine]

    logger.debug(
        f"Reconstruction: top {n_refine} coarse candidates: "
        + ", ".join(f"step={s:.0f} err={e:.2f}" for e, s, _, _ in top_coarse)
    )

    refined: List[Tuple[float, float, float, float]] = []
    for error, step, ox, oy in top_coarse:
        r_error, r_step, r_ox, r_oy = _refine_grid_nelder_mead(
            integral_sum, integral_sq, integral_count,
            width, height, step, ox, oy,
        )
        refined.append((r_error, r_step, r_ox, r_oy))

    refined.sort(key=lambda x: x[0])
    return refined


def generate_reconstruction_step_estimates(
    img: Image.Image,
    width: int,
    height: int,
    config: Config,
    max_candidates: int = 3,
) -> List[Tuple[float, str]]:
    """Estimate optimal grid step sizes using reconstruction error minimization.

    Returns step size estimates (not cut lists) so the caller can use its own
    edge-snapped cut placement. This gives the best of both worlds:
    reconstruction's step finding + gradient-based boundary placement.

    Args:
        img: Original RGBA image (NOT quantized).
        width: Image width.
        height: Image height.
        config: Configuration options.
        max_candidates: Maximum step estimates to return.

    Returns:
        List of (step_size, source_label) tuples, sorted by reconstruction error.
    """
    setup = _setup_reconstruction(img, width, height, config)
    if setup is None:
        return []

    integral_sum, integral_sq, integral_count, min_step, max_step, resolution_hint, offset_stride = setup

    refined = _coarse_search_and_refine(
        integral_sum, integral_sq, integral_count,
        width, height, min_step, max_step,
        offset_stride, resolution_hint, max_candidates,
    )
    if not refined:
        return []

    # Deduplicate by rounded step (avoid near-identical candidates)
    results: List[Tuple[float, str]] = []
    seen_steps: set = set()

    for error, step, ox, oy in refined[:max_candidates * 2]:
        # Round to 0.5 to dedup near-identical steps
        step_key = round(step * 2) / 2
        if step_key in seen_steps:
            continue
        seen_steps.add(step_key)

        # Check resolution hint
        if resolution_hint is not None:
            cells_x = len(_make_uniform_cuts(step, 0.0, width)) - 1
            cells_y = len(_make_uniform_cuts(step, 0.0, height)) - 1
            if max(cells_x, cells_y) > resolution_hint:
                continue

        cells_x = len(_make_uniform_cuts(step, ox, width)) - 1
        cells_y = len(_make_uniform_cuts(step, oy, height)) - 1
        source = f"recon(s={step:.1f},{cells_x}x{cells_y})"

        logger.debug(f"Reconstruction step estimate: {source}, error={error:.4f}")
        results.append((step, source))

        if len(results) >= max_candidates:
            break

    return results


def generate_reconstruction_candidates(
    img: Image.Image,
    width: int,
    height: int,
    config: Config,
    max_candidates: int = 3,
) -> List[Tuple[List[int], List[int], float, str]]:
    """Generate grid candidates using reconstruction error minimization.

    Searches over step sizes and offsets to find the grid that best explains
    the observed image as a pixelated grid. Uses integral images for O(1)
    per-cell variance queries.

    The algorithm:
    1. Converts image to perceptual color space
    2. Builds integral images for fast variance computation
    3. Coarse search over integer step sizes and offsets
    4. Sub-pixel refinement via Nelder-Mead on top candidates
    5. Non-uniform local correction of grid boundaries
    6. Returns top candidates sorted by reconstruction error

    Args:
        img: Original RGBA image (NOT quantized).
        width: Image width.
        height: Image height.
        config: Configuration options.
        max_candidates: Maximum candidates to return.

    Returns:
        List of (col_cuts, row_cuts, step_size, source) candidate tuples.
    """
    setup = _setup_reconstruction(img, width, height, config)
    if setup is None:
        return []

    integral_sum, integral_sq, integral_count, min_step, max_step, resolution_hint, offset_stride = setup

    refined = _coarse_search_and_refine(
        integral_sum, integral_sq, integral_count,
        width, height, min_step, max_step,
        offset_stride, resolution_hint, max_candidates,
    )
    if not refined:
        return []

    # Take top candidates and apply phase 3 correction
    candidates: List[Tuple[List[int], List[int], float, str]] = []
    seen_grids: set = set()

    for error, step, ox, oy in refined[:max_candidates]:
        col_cuts = _make_uniform_cuts(step, ox, width)
        row_cuts = _make_uniform_cuts(step, oy, height)

        # Phase 3: Local non-uniform correction
        col_cuts = _local_grid_correction(
            integral_sum, integral_sq, integral_count,
            col_cuts, width,
            search_radius=max(1, min(int(step * 0.15), 3)),
            is_cols=True,
            other_cuts=row_cuts,
        )
        row_cuts = _local_grid_correction(
            integral_sum, integral_sq, integral_count,
            row_cuts, height,
            search_radius=max(1, min(int(step * 0.15), 3)),
            is_cols=False,
            other_cuts=col_cuts,
        )

        # Deduplicate
        grid_key = (tuple(col_cuts), tuple(row_cuts))
        if grid_key in seen_grids:
            continue
        seen_grids.add(grid_key)

        cells_x = len(col_cuts) - 1
        cells_y = len(row_cuts) - 1

        # Enforce resolution hint after local correction
        if resolution_hint is not None and max(cells_x, cells_y) > resolution_hint:
            logger.debug(
                f"Reconstruction: skipping corrected grid {cells_x}x{cells_y} "
                f"(exceeds hint={resolution_hint})"
            )
            continue

        source = f"recon(s={step:.1f},{cells_x}x{cells_y})"

        logger.debug(
            f"Reconstruction candidate: {source}, error={error:.2f}"
        )

        candidates.append((col_cuts, row_cuts, step, source))

    return candidates
